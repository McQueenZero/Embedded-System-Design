void TIM2_IRQHandler(void)
{
	extern char data_h;
	extern char data_l;
	extern unsigned int flag;	//0.5秒高低位轮发标志位
	extern unsigned int sendflag;	//发送完成标志位
	
	TIM_ClearITPendingBit(TIM2,TIM_FLAG_Update);	

	if(flag==0)
	{
		flag=1;
		USART_SendData(USART3,data_h);	//先发送高8位
	}
	else
	{
		flag=0;
		USART_SendData(USART3,data_l);	//后发送低8位
		sendflag=1;	//12位发送完，发送标志位置1
	}
	
}
